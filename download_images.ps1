$outputFolder = "$pwd\public\images"

Write-Host "Starting download of images into: $outputFolder"
foreach ($key in $images.Keys) {
    $url = $images[$key]
    $destination = Join-Path -Path $outputFolder -ChildPath $key
    if (-Not (Test-Path $destination)) {
        Write-Host "Downloading $key ..."
        Invoke-WebRequest -Uri $url -OutFile $destination
    } else {
        Write-Host "Skipping $key (already exists)."
    }
}
Write-Host "Done downloading images."
